#!/usr/bin/env python3
"""
Real IR Download Helper for Auto Gain Staging Benchmarks.

OpenAIR (https://www.openair.hosted.york.ac.uk/) hosts a large collection of
acoustic impulse responses, but does not provide a programmatic download API.
Each IR is on its own WordPress post page with a WAV file attachment.

This script provides:
1. A list of recommended IRs for download (organized by category)
2. Instructions for downloading from the OpenAIR website
3. A fallback mode using the already-downloaded or provided IR files

Usage:
  python download_real_irs.py sampledata/real_irs

Manual download steps:
  1. Browse https://www.openair.hosted.york.ac.uk/?page_id=36
  2. Click on each IR name to open its page
  3. Find the WAV download link (usually in the post content)
  4. Save to sampledata/real_irs/
"""

