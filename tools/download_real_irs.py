def check_existing_irs(output_dir):
    """Check which IRs already exist in the output directory."""
    if not output_dir.exists():
        return []
    return list(output_dir.glob("*.wav"))

def main():
    output_dir = Path(sys.argv[1]) if len(sys.argv) > 1 else Path("sampledata/real_irs")
    output_dir.mkdir(parents=True, exist_ok=True)

    print("=" * 70)
    print("  Real IR Download Helper")
    print("=" * 70)
    print()
    print(f"  OpenAIR does not provide direct WAV download URLs.")
    print(f"  Manual download from the OpenAIR website is required.")
    print(f"  See: https://www.openair.hosted.york.ac.uk/?page_id=36")
    print()
    print(f"  Recommended IRs for benchmarking (50 total):")
    print()

    for category, irs in RECOMMENDED_IRS.items():
        print(f"  [{category}]")
        for ir in irs:
            print(f"    - {ir}")
        print()

    print(f"  Output directory: {output_dir.resolve()}")

    # Check for existing files
    existing = check_existing_irs(output_dir)
    if existing:
        print(f"\n  Already present: {len(existing)} files")
        for f in existing:
            try:
                with wave.open(str(f), 'r') as w:
                    dur = w.getnframes() / w.getframerate()
                    print(f"    {f.name}: {w.getnframes()}samples, {dur:.1f}s, {w.getnchannels()}ch")
            except:
                print(f"    {f.name}: (unreadable)")
    else:
        print(f"\n  No IR files found yet. Download manually from OpenAIR.")
        print()
        print(f"  Alternatively, synthetic IRs are available in:")
        print(f"    sampledata/synthetic/ (22 files)")

    print(f"\n{'=' * 70}")
    print(f"  Synthetic IRs generated by tools/generate_synthetic_irs.py")
    print(f"{'=' * 70}")
    main()
